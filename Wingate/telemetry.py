miles = int(input('Please input number of miles: '))

yards = miles * 1760
feet = yards * 3
inches = feet * 12

print(f'yards: {yards}, feet: {feet}, inches: {inches}')

km = int(input("Please input number of km's: "))

meters = km * 1000
centi = meters * 100
milli = centi * 10

print(f' meters: {meters}, centimeters: {centi}, millimeters: {milli}')
